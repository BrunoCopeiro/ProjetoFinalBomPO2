    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       // captura de dados na tela Usuario
        int id = Integer.parseInt(txtId.getText());
        String cliente = txtCliente.getText();
        String data = txtData.getText();
        String hora = txtHora.getText();
            String descricao = txtDescricao.getText();

        //transferencia de dados para UsuariosDTO
        AgendaDTO objAgendaDTO = new AgendaDTO();
        objAgendaDTO.setId(id);
        objAgendaDTO.setCliente(cliente);
        objAgendaDTO.setData(data);
        objAgendaDTO.setHora(hora);
        objAgendaDTO.setDescricao(descricao);

        //criação do objeto da classe UsuarioDAO para inserir o usuario
       AgendaDAO objAgendaDAO = new AgendaDAO();
        objAgendaDAO.inserirAgenda(objAgendaDTO);

        txtCliente.setText("");
        txtData.setText("");
        txtHora.setText("");
         txtDescricao.setText("");
        txtId.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

