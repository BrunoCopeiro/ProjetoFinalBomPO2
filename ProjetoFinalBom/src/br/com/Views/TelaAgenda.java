    public void pesquisa() {
        String sql = "select * from tb_agenda where id = ?";

        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtId.getText());
            rs = pst.executeQuery();

            if (rs.next()) {
                txtCliente.setText(rs.getString(2));
                txtData.setText(rs.getString(3));
                txtHora.setText(rs.getString(4));
                                txtDescricao.setText(rs.getString(5));

            } else {

                JOptionPane.showMessageDialog(null, "Agenda não cadastrada!");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Metodo Pesquisar" + e);
        }

    }

